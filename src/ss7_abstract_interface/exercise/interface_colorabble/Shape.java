package ss7_abstract_interface.exercise.interface_colorabble;

public class Shape {
    private String color = " blue";

    public Shape(){

    };

    public Shape(String color) {
        this.color = color;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    @Override
    public String toString() {
        return "Shape{" +
                "color='" + color + '\'' +
                '}';
    }
}

